class Solution {
  public:
    // Function to count the number of digits in n that evenly divide n
    int evenlyDivides(int n) {
        int cnt =0;
        int rem =0;
        int original = n;
        
        while(n>0){
            rem = n%10;
            if(rem!=0 && original%rem==0){
            cnt+=1;}
            n=n/10;
        }
        return cnt;
    }
};

